/**
 * checkId 조회 헬퍼 — 'l01-check1' 과 'lesson01-check1' 두 형식 모두 지원
 */
export function getKCQuestions(checkId: string): KCQuestion[] | null {
  if (KC_QUESTIONS[checkId]) return KC_QUESTIONS[checkId];

  // 'lesson01-check1' → 'l01-check1' 변환 시도
  const shortMatch = checkId.match(/^lesson(\d+)-(.+)$/);
  if (shortMatch) {
    const shortId = `l${shortMatch[1]}-${shortMatch[2]}`;
    if (KC_QUESTIONS[shortId]) return KC_QUESTIONS[shortId];
  }

  // 'l01-check1' → 'lesson01-check1' 변환 시도
  const longMatch = checkId.match(/^l(\d+)-(.+)$/);
  if (longMatch) {
    const longId = `lesson${longMatch[1].padStart(2, '0')}-${longMatch[2]}`;
    if (KC_QUESTIONS[longId]) return KC_QUESTIONS[longId];
  }

  return null;
}
